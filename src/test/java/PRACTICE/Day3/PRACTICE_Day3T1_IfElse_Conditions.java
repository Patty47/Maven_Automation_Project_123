package PRACTICE.Day3;

public class PRACTICE_Day3T1_IfElse_Conditions {
    public static void main(String[] args) {
        //declare variables for if else condition
        int a, b;
        a = 5;
        b = 2;

        //find if a is less than b
        if (a < b) {
            System.out.println("a is less than b");
        } else {
            System.out.println("a is either greater than or equal to b");
        }//end of if else condition
    }//end of main
}//end of class
